document.addEventListener("DOMContentLoaded", () => {
  // Prevent scrolling initially
  document.body.setAttribute("data-lenis-prevent", true);

  const loaderDiv = document.querySelector(".loader");
  if (!loaderDiv) {
    console.error("Loader div not found");
    return;
  }

  const loaderProgress = document.createElement("div");
  const logoContainer = loaderDiv.querySelector(".logo-container");
  loaderProgress.classList.add("loader__progress");
  logoContainer.appendChild(loaderProgress);

  let loadedCount = 0;
  const totalImages = nalaImagePaths.length;

  // Update loader percentage
  function updateLoader() {
    const percentage = Math.round((loadedCount / totalImages) * 100);
    loaderProgress.textContent = `${percentage}%`;
  }

  // Load a single image and return a promise
  function loadImage(src) {
    return new Promise((resolve, reject) => {
      const img = new Image();

      img.onload = () => {
        loadedCount++;
        updateLoader();
        resolve(src);
      };

      img.onerror = () => {
        loadedCount++;
        updateLoader();
        console.warn(`Failed to load image: ${src}`);
        resolve(src); // Resolve anyway to not block the loader
      };

      img.src = src;
    });
  }

  // Initial state
  updateLoader();

  // Load all images
  const imagePromises = nalaImagePaths.map((path) => loadImage(path));

  Promise.all(imagePromises).then(() => {
    // alert("Finished");
    // document.body.classList.remove("loading");

    // Allow scrolling now that all images are loaded
    document.body.removeAttribute("data-lenis-prevent");
  });
});
